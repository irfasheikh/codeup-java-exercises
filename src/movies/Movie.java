package movies;
// a constuctor creates a new object of the class
//constructors, getters, setters
// like for new person, a method called person which is now the constuctor class
public class Movie {
    private String name;
    private String category;

    //Name getter
    public String getName(){
        return name;
    }

    // Name Setter
    public void setName(String newName) {
        this.name = newName;
    }

    //Category GETTER

    public String getCategory(){
        return category;
    }
//category setter
    public void setCategory(String newCategory){
        this.category = newCategory;
    }




}

